    private void btnHesaplaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHesaplaActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("OdevPU");
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("select f from Fatura f");
        List<Fatura> faturalar = q.getResultList();
        int toplamAbone = 0;
        double ortalamaUcret = 0;
        int toplamTutar = 0;

        toplamAbone = dtm.getRowCount();
        for (Fatura f : faturalar) {
            toplamTutar += f.getFaturatutari();
            ortalamaUcret = (double) toplamTutar / (double) toplamAbone;

        }
        txtOrtalamaOdeme.setText("Ödenen ortalama fatura ücreti = " + String.valueOf(ortalamaUcret) + " TL'dir.");

        em.close();
        emf.close();
    }//GEN-LAST:event_btnHesaplaActionPerformed

