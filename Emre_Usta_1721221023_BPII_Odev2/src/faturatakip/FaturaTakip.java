    private void btnSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSilActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("OdevPU");
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("delete from Fatura f WHERE f.faturano = :faturano");
        Query q1= em.createQuery("select f1 from Fatura f1");
        q.setParameter("faturano", Integer.valueOf(txtFaturaNo.getText()));
        
        List<Fatura> faturalar = q1.getResultList();
        
        for (Fatura f : faturalar) {
            if(!txtFaturaNo.getText().equals(String.valueOf(f.getFaturano()))){
                JOptionPane.showMessageDialog(this, "Girilen Fatura Numarası Sistemde Yer Almamaktadır.");
            }
        }
        em.getTransaction().begin();
        q.executeUpdate();
        em.getTransaction().commit();
        em.close();
        emf.close();


    }//GEN-LAST:event_btnSilActionPerformed

