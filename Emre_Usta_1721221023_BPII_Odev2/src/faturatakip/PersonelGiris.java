    private void btnGonderPersonelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGonderPersonelActionPerformed
        // TODO add your handling code here:
        Pattern tcDesenP = Pattern.compile("[0-9]");
        Matcher tcYakalaP = tcDesenP.matcher(txtTcPersonel.getText());
        if (!tcYakalaP.find()) {

            lblTCDurumPersonel.setText("Geçerli Bir Tc Kimlik Numarası Giriniz.");

        } else {
            lblTCDurumPersonel.setText("");
        }
        Pattern adDesenP = Pattern.compile("^[a-zA-Z]+$");
        Matcher adYakalaP = adDesenP.matcher(txtAdiPersonel.getText());
        if (!adYakalaP.find()) {
            lblAdıDurumPersonel.setText("Geçerli Ad Giriniz.");
        } else {
            lblAdıDurumPersonel.setText("");
        }
        Pattern soyadDesenP = Pattern.compile("^[a-zA-Z]+$");
        Matcher soyadYakalaP = soyadDesenP.matcher(txtSoyadiPersonel.getText());
        if (!soyadYakalaP.find()) {
            lblSoyadiDurumPersonel.setText("Geçerli Soyad Giriniz.");
        } else {
            lblSoyadiDurumPersonel.setText("");
        }
        Pattern personelNoDesen = Pattern.compile("[0-9]");
        Matcher personelNoYakala = personelNoDesen.matcher(txtPersonelNoPersonel.getText());
        if (!personelNoYakala.find()) {

            lblPersonelNoDurumP.setText("Geçerli Bir Personel Numarası Giriniz.");

        } else {
            lblPersonelNoDurumP.setText("");
        }
        Pattern subeDesen = Pattern.compile("^[a-zA-Z]+$");
        Matcher subeYakala = subeDesen.matcher(txtSubePersonel.getText());
        if (!subeYakala.find()) {
            lblŞubeDurumPersonel.setText("Geçerli Şube Giriniz.");
        } else {
            lblŞubeDurumPersonel.setText("");
        }
        
        
        try {
            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/Odev ", "odev", "odev");
            String sqlSorgu = "SELECT * FROM personel WHERE TC=?";
            PreparedStatement psSorgu = con.prepareStatement(sqlSorgu);
            psSorgu.setInt(1, Integer.parseInt(txtTcPersonel.getText()));
            ResultSet rs = psSorgu.executeQuery();

            if (rs.next()) {
                String sqlUpdate = "UPDATE personel" + "SET ADI=?,SOYADI=?,PERSONELNO=?,ŞUBE=?" + "WHERE TC=?";
                PreparedStatement psUpdate = con.prepareStatement(sqlUpdate);
                psUpdate.setInt(1, Integer.parseInt(txtTcPersonel.getText()));
                psUpdate.setString(2, txtAdiPersonel.getText());
                psUpdate.setString(3, txtSoyadiPersonel.getText());
                psUpdate.setInt(4, Integer.parseInt(txtPersonelNoPersonel.getText()));
                
                psUpdate.setString(5, txtSubePersonel.getText());
                psUpdate.executeUpdate();
            } else {
                String sqlInsert = "INSERT INTO PERSONEL "
                        + " (TC,ADI,SOYADI,PERSONELNO,ŞUBE) "
                        + " VALUES (?,?,?,?,?)";
                PreparedStatement psInsert = con.prepareStatement(sqlInsert);

                psInsert.setInt(1, Integer.parseInt(txtTcPersonel.getText()));
                psInsert.setString(2, txtAdiPersonel.getText());
                psInsert.setString(3, txtSoyadiPersonel.getText());
                psInsert.setInt(4, Integer.parseInt(txtPersonelNoPersonel.getText()));
               
                psInsert.setString(5, txtSubePersonel.getText());
                psInsert.executeUpdate();

                txtTcPersonel.setText("");
                txtAdiPersonel.setText("");
                txtSoyadiPersonel.setText("");
                txtPersonelNoPersonel.setText("");
                txtSubePersonel.setText("");
                

            }
        } catch (SQLException ex) {
            Logger.getLogger(PersonelGiris.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGonderPersonelActionPerformed

