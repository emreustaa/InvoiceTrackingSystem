    private void btnGirisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGirisActionPerformed

        try {
            // TODO add your handling code here:
            Pattern tcDesen = Pattern.compile("[0-9]");
            Pattern personelNoDesen = Pattern.compile("[0-9]");
            Matcher tcYakala = tcDesen.matcher(txtTc.getText());
            Matcher personelYakala = personelNoDesen.matcher(txtPersonelNo.getText());
            if (!tcYakala.find()) {

                lblTcDurum.setText("Yanlış Bilgi Girilmiştir.");

            } else {
                lblTcDurum.setText("");
            }
            if (!personelYakala.find()) {

                lblPersonelDurum.setText("Yanlış Bilgi Girilmiştir.");

            } else {
                lblPersonelDurum.setText("");
            }

            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/Odev ", "odev", "odev");
            String sqlSorgu = "SELECT * FROM PERSONEL WHERE TC=?";
            PreparedStatement psSorgu = con.prepareStatement(sqlSorgu);
            psSorgu.setInt(1, Integer.parseInt(txtTc.getText()));
            ResultSet rs = psSorgu.executeQuery();

            if (rs.next()) {

                String TC = String.valueOf(rs.getInt("TC"));
                String PERSONELNO = String.valueOf(rs.getInt("PERSONELNO"));
                if (TC.equals(txtTc.getText()) && PERSONELNO.equals(txtPersonelNo.getText())) {
                    FaturaTakip ft = new FaturaTakip();

                    ft.setVisible(true);
                    this.dispose();

                } else /*if(txtTc.getText()==" " && txtPersonelNo.getText()==" ")*/ {
                    JOptionPane.showMessageDialog(this, "Bilgiler Doğru Girilmemiştir..");
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(PersonelGiris.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGirisActionPerformed

